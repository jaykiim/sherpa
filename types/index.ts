import React from "react";

export interface RootProps {
  drawer?: boolean;
  setDrawer?: React.Dispatch<React.SetStateAction<boolean>>;
  modal?: boolean;
  setModal?: React.Dispatch<React.SetStateAction<Modal>>;
}

/* ==========================================================================================================================================================================================================================================
* users
========================================================================================================================================================================================================================================== */

export interface User {
  id: string;
  name: string;
  projects: string[] | [];
}

/* ==========================================================================================================================================================================================================================================
* projects
========================================================================================================================================================================================================================================== */

export interface Projects {
  [id: string]: Project;
}

export interface Project {
  id: string;
  name: string;
  start: string;
  end: string;
  keyresults: string[];
}

/* ==========================================================================================================================================================================================================================================
* key results
========================================================================================================================================================================================================================================== */

export interface KeyResult {
  id: string;
  name: string;
  color: string;
  projectId: string;
  tools: {
    tasks: string[] | [];
    plans: string[] | [];
  };
}

/* ==========================================================================================================================================================================================================================================
* utils
========================================================================================================================================================================================================================================== */

export interface Modal {
  desc: string;
  size: string;
  open: boolean;
}
