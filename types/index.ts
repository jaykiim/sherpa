/* ==========================================================================================================================================================================================================================================
* users
========================================================================================================================================================================================================================================== */

export interface User {
  id: string;
  name: string;
  projects: string[] | [];
}

/* ==========================================================================================================================================================================================================================================
* projects
========================================================================================================================================================================================================================================== */

export interface Projects {
  [id: string]: Project;
}

export interface Project {
  id: string;
  name: string;
  start: string;
  end: string;
  keyresults: string[];
}

/* ==========================================================================================================================================================================================================================================
* key results
========================================================================================================================================================================================================================================== */

export interface KeyResult {
  id: string;
  name: string;
  color: string;
  projectId: string;
  tools: {
    tasks: string[] | [];
    plans: string[] | [];
  };
}
