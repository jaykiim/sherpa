import React from "react";

export interface RootProps {
  drawer?: boolean;
  setDrawer?: React.Dispatch<React.SetStateAction<boolean>>;
  modal?: boolean;
  setModal?: React.Dispatch<React.SetStateAction<Modal>>;
}

/* ==========================================================================================================================================================================================================================================
* users
========================================================================================================================================================================================================================================== */

export interface User {
  id: string;
  name: string;
  projects: string[] | [];
}

/* ==========================================================================================================================================================================================================================================
* projects
========================================================================================================================================================================================================================================== */

export interface Projects {
  [id: string]: Project;
}

export interface Project {
  id: string;
  name: string;
  start: string;
  end: string;
  keyresults: string[];
}

/* ==========================================================================================================================================================================================================================================
* key results
========================================================================================================================================================================================================================================== */

export interface KeyResult {
  id: string;
  name: string;
  color: string;
  projectId: string;
  tools: string[] | [];
  tasks: string[] | [];
  plans: string[] | [];
}

/* ==========================================================================================================================================================================================================================================
* tasks
========================================================================================================================================================================================================================================== */

export interface Tasks {
  [taskId: string]: Task;
}

export interface Task {
  id: string;
  name: string;
  leadTime: string;
  actualTime: string;
  done: boolean;
  records: {
    [date: string]: string[];
  };
  keyresultId: string;
}

/* ==========================================================================================================================================================================================================================================
* records
========================================================================================================================================================================================================================================== */

export interface Records {
  [recordId: string]: Record;
}

export interface Record {
  id: string;
  name: string;
  time: string;
  taskId: string;
}

/* ==========================================================================================================================================================================================================================================
* utils
========================================================================================================================================================================================================================================== */

// modal -------------------------------------------------------------------------

export interface Modal {
  desc: string;
  size: string;
  open: boolean;
}
